package com.exc.web.rest;

import com.exc.UserApp;

import com.exc.config.SecurityBeanOverrideConfiguration;

import com.exc.domain.UserInfo;
import com.exc.repository.UserInfoRepository;
import com.exc.service.UserInfoService;
import com.exc.service.dto.UserInfoDTO;
import com.exc.service.mapper.UserInfoMapper;
import com.exc.web.rest.errors.ExceptionTranslator;

import org.junit.Before;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.mockito.MockitoAnnotations;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.data.web.PageableHandlerMethodArgumentResolver;
